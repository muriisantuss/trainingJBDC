package beans;


public class Vendas {
    private int vendasId;
    private String data;
    private int total;


    public int getVendasId() {
        return vendasId;
    }

    public void setVendasId(int vendasId) {
        this.vendasId = vendasId;
    }

    public String getData() {
        return data;
    }

    public void setData(String data) {
        this.data = data;
    }

    public int getTotal() {
        return total;
    }

    public void setTotal(int total) {
        this.total = total;
    }

}
